// The machine-readable pack-contract artifact (2026-07 staff review, Pass 4).
//
// External toolchains — editor integrations, CLI diagnostics, remote LLM
// authoring loops — need the contract in a form they can consume without
// importing this repo's TypeScript. This emits docs/pack-contract.json:
// the contract version, the engine's REAL vocabularies (imported from the
// built engine — runtime truth, never a copy), and the validator's full
// issue-code catalog (extracted from js/validate.ts source, so a new check
// lands in the catalog automatically).
//
// The artifact is committed and pinned like a golden: test/contract-artifact
// .test.mjs regenerates it and diffs, so drift fails CI. Regenerate with:
//
//   npm run build && node tools/gen-contract-artifact.mjs
//
// Deliberately NOT a JSON Schema: validatePack is already the hostile-input
// parse layer (never-throws, total over malformed shapes), and a parallel
// hand-maintained schema would drift from it. What external tools actually
// need is what this ships — the vocabularies to generate against and the
// issue codes to key repair logic on.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

export async function buildContractArtifact() {
  const { CORE_EFFECT_VERBS, REQUIRES_NEUTRAL_KEYS } = await import('../dist/js/engine.js');
  const { PACK_CONTRACT_VERSION, TIERS, SIDES } = await import('../dist/js/validate.js');
  const { SAVE_SCHEMA_VERSION, EXPORT_CODE_VERSION } = await import('../dist/js/save.js');

  // Issue-code catalog: every .error/.warn call site in the validator source.
  // Severity is recorded per code; a code emitted at both severities
  // (act-out-of-range on an event vs. a tutorial event, say) lists both.
  //
  // Two spellings are extractable, and ONLY these two:
  //   c.error('some-code', …)                                — a literal
  //   c.error(cond ? 'code-a' : 'code-b', …)                 — two literals
  // The completeness claim is ENFORCED, not hoped for: any other first
  // argument (a template literal, a variable) is invisible to this extractor,
  // so it THROWS — which fails the drift test and CI — instead of emitting a
  // knowingly-incomplete catalog. (This guard exists because the first
  // version of the catalog silently missed four template-literal codes.)
  const src = readFileSync(join(ROOT, 'js/validate.ts'), 'utf8');
  const issues = {};
  const CODE = "'([a-z0-9-]+)'";
  const site = new RegExp(
    String.raw`\.(error|warn)\(\s*(?:${CODE}|[^()?]+\?\s*${CODE}\s*:\s*${CODE})\s*,`, 'g');
  let sites = 0;
  for (const [, sev, lit, ternA, ternB] of src.matchAll(site)) {
    sites++;
    const severity = sev === 'warn' ? 'warning' : 'error';
    for (const code of [lit, ternA, ternB]) {
      if (code) (issues[code] ??= new Set()).add(severity);
    }
  }
  const allSites = [...src.matchAll(/\.(?:error|warn)\(/g)].length;
  if (sites !== allSites) {
    throw new Error(`gen-contract-artifact: ${allSites - sites} validator call site(s) carry an issue code this extractor can't read (not a quoted literal or a two-literal ternary). Spell the code literally so the published catalog stays complete.`);
  }
  const issueCodes = Object.fromEntries(
    Object.keys(issues).sort().map((c) => [c, [...issues[c]].sort()]),
  );

  return {
    $comment: 'GENERATED by tools/gen-contract-artifact.mjs — do not edit. Pinned by test/contract-artifact.test.mjs.',
    packContractVersion: PACK_CONTRACT_VERSION,
    saveSchemaVersion: SAVE_SCHEMA_VERSION,
    exportCodeVersion: EXPORT_CODE_VERSION,
    // What a generated pack may use WITHOUT declaring anything: the engine's
    // core effect verbs and the genre-neutral requires keys. Everything else
    // (pack stats, resources, flags, plugin verbs/keys) is declared by the
    // pack itself and validated against its own declarations.
    coreEffectVerbs: [...CORE_EFFECT_VERBS].sort(),
    requiresNeutralKeys: [...REQUIRES_NEUTRAL_KEYS].sort(),
    tiers: [...TIERS],
    sides: [...SIDES],
    // Every code validatePack can emit, with the severities it uses. Stable
    // keys for external repair loops; see js/validate.ts for the messages.
    issueCodes,
  };
}

export function renderContractArtifact(artifact) {
  return JSON.stringify(artifact, null, 2) + '\n';
}

export const ARTIFACT_PATH = join(ROOT, 'docs/pack-contract.json');

const isMain = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];
if (isMain) {
  const out = renderContractArtifact(await buildContractArtifact());
  writeFileSync(ARTIFACT_PATH, out);
  console.log(`wrote docs/pack-contract.json (${out.length} bytes)`);
}
